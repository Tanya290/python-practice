#python casting:
#specify a variable type
#int()-constructs an integer number from an integer literal,a float literal(by removing all decimals)or a string literal
#float()-constructs a float number from an integer literal or a string literal or float literal
#str()-constructs a string from a wide variety of data types,including strings,integer and float literals

#integers
x=int(1) #x will be 1
y=int(2.8) #y will be 2
z=int("3") #z will be 3

#floats
x=float(1) #x will be 1.0
y=float(2.8) #y will be 2.8
z=float("3") #z will be 3.0

#strings
x=str("s1") #x will be 's1'
y=str(2) #y will be '2'
z=str(3.0) #z will be '3.0'